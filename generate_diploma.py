def find_ot_md() -> Path | None:
    exact = BASE / 'Охрана_труда_Барановичиводоканал.md'
    if exact.exists():
        return exact
    candidates = sorted(BASE.glob('*.md'), key=lambda p: p.stat().st_mtime, reverse=True)
    for path in candidates:
        name = path.name.lower()
        if 'охрана' in name and 'труда' in name:
            return path
    return None


def transform_ot(text: str) -> str:
    text = text.replace('\r\n', '\n')
    text = re.sub(r'^#\s*6\s+.*$', '# 7 ОХРАНА ТРУДА', text, flags=re.M)
    text = re.sub(r'(?m)^(#{2,6}\s+)6(\.\d+(?:\.\d+)*)', r'\g<1>7\2', text)
    text = text.replace('Таблица 6.', 'Таблица 7.')
    text = text.replace('рисунке 6.', 'рисунке 7.')
    text = text.replace('Рисунок 6.', 'Рисунок 7.')
    text = text.replace('## Список источников к разделу', '## 7.7 Список источников к разделу')
    return text.strip()


def build_markdown() -> str:
    ot_path = find_ot_md()
    if ot_path and ot_path.exists():
        ot_text = transform_ot(ot_path.read_text(encoding='utf-8'))
    else:
        ot_text = '''# 7 ОХРАНА ТРУДА
Раздел охраны труда подлежит уточнению на основании методических указаний и данных предприятия.'''
    return INTRO_MD.strip() + '\n\n' + ot_text + '\n\n' + CONCLUSION_MD.strip() + '\n'


def set_cell_text(cell, text, bold=False):
    cell.text = ''
    p = cell.paragraphs[0]
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER if bold else WD_ALIGN_PARAGRAPH.LEFT
    run = p.add_run(text.strip())
