def add_paragraph(doc, text, *, list_item=False):
    p = doc.add_paragraph(style='Normal')
    stripped = text.strip()
    is_caption = stripped.startswith(('Таблица ', 'Рисунок '))
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER if stripped.startswith('Рисунок ') else WD_ALIGN_PARAGRAPH.JUSTIFY
    p.paragraph_format.line_spacing = 1.5
    p.paragraph_format.space_after = Pt(0)
    if list_item:
        p.paragraph_format.first_line_indent = Cm(0)
        p.paragraph_format.left_indent = Cm(1.25)
        run = p.add_run('- ' + stripped)
    elif is_caption:
        p.paragraph_format.first_line_indent = Cm(0)
        run = p.add_run(stripped)
    else:
        p.paragraph_format.first_line_indent = Cm(1.25)
        run = p.add_run(stripped)
    set_run_font(run, size=14)


def add_heading(doc, text, level, first_h1):
    if level == 1 and not first_h1:
        doc.add_page_break()
    p = doc.add_paragraph(style=f'Heading {level}')
    if level == 1:
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        p.paragraph_format.first_line_indent = Cm(0)
    else:
        p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
        p.paragraph_format.first_line_indent = Cm(1.25)
    run = p.add_run(text.strip())
    set_run_font(run, size=14, bold=True)
    return False if level == 1 else first_h1


def parse_table(lines):
    rows = []
    for line in lines:
        stripped = line.strip()
        if not stripped.startswith('|') or not stripped.endswith('|'):
            continue
        cells = [c.strip() for c in stripped.strip('|').split('|')]
        if all(re.fullmatch(r':?-{3,}:?', c or '') for c in cells):
            continue
        rows.append(cells)
    return rows


def add_table(doc, rows):
    if not rows:
        return
    cols = max(len(row) for row in rows)
    table = doc.add_table(rows=len(rows), cols=cols)
    table.style = 'Table Grid'
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    table.autofit = True
    for i, row in enumerate(rows):
        for j in range(cols):
            text = row[j] if j < len(row) else ''
            set_cell_text(table.cell(i, j), text, bold=(i == 0))
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(0)
    p.paragraph_format.line_spacing = 1.0


def add_image(doc, alt, image_path):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.first_line_indent = Cm(0)
    path = Path(image_path)
    if path.exists():
        run = p.add_run()
        try:
            run.add_picture(str(path), width=Cm(15.5))
        except Exception:
            add_paragraph(doc, f'[{alt}: изображение не удалось вставить]')
            return
    else:
        add_paragraph(doc, f'[{alt}: файл изображения не найден]')
        return
    cap = doc.add_paragraph()
    cap.alignment = WD_ALIGN_PARAGRAPH.CENTER
    cap.paragraph_format.first_line_indent = Cm(0)
    cap.paragraph_format.line_spacing = 1.5
    cap.paragraph_format.space_after = Pt(0)
    run = cap.add_run(alt)
    set_run_font(run, size=14)


def markdown_to_docx(markdown: str, out_path: Path):
    doc = Document()
    configure_document(doc)
    first_h1 = True
    lines = markdown.splitlines()
    i = 0
    while i < len(lines):
        line = lines[i].rstrip()
        stripped = line.strip()
        if not stripped:
            i += 1
            continue
        if stripped.startswith('|') and stripped.endswith('|'):
            block = []
            while i < len(lines) and lines[i].strip().startswith('|') and lines[i].strip().endswith('|'):
                block.append(lines[i])
                i += 1
            add_table(doc, parse_table(block))
            continue
        img = re.fullmatch(r'!\[(.+?)\]\((.+?)\)', stripped)
        if img:
            add_image(doc, img.group(1), img.group(2))
            i += 1
            continue
        if stripped == '[TOC]':
            add_toc(doc.add_paragraph())
            i += 1
            continue
        m = re.match(r'^(#{1,3})\s+(.+)$', stripped)
        if m:
            level = len(m.group(1))
            first_h1 = add_heading(doc, m.group(2), level, first_h1)
            i += 1
            continue
        if stripped.startswith('- '):
            add_paragraph(doc, stripped[2:], list_item=True)
        else:
            add_paragraph(doc, stripped)
        i += 1
    doc.save(out_path)


